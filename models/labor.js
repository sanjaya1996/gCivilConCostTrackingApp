class Labor {
  constructor(
    id,
    firstName,
    lastName,
    email,
    phoneNumber,
    role,
    availability,
    hourlyRate,
    accountDetails
  ) {
    this.id = id;
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.phoneNumber = phoneNumber;
    this.role = role;
    this.availability = availability;
    this.hourlyRate = hourlyRate;
    this.accountDetails = accountDetails;
  }
}

export default Labor;
